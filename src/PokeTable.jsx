interface TablePaginationActionsProps {
  count: number;
  page: number;
  rowsPerPage: number;
  onPageChange: (
    event: React.MouseEvent<HTMLButtonElement>,
    newPage: number
  ) => void;
}

function TablePaginationActions(props: TablePaginationActionsProps) {
  const theme = useTheme();
  const { count, page, rowsPerPage, onPageChange } = props;

  const handleFirstPageButtonClick = (
    event: React.MouseEvent<HTMLButtonElement>
  ) => {
    onPageChange(event, 0);
  };

  const handleBackButtonClick = (
    event: React.MouseEvent<HTMLButtonElement>
  ) => {
    onPageChange(event, page - 1);
  };

  const handleNextButtonClick = (
    event: React.MouseEvent<HTMLButtonElement>
  ) => {
    onPageChange(event, page + 1);
  };

  const handleLastPageButtonClick = (
    event: React.MouseEvent<HTMLButtonElement>
  ) => {
    onPageChange(event, Math.max(0, Math.ceil(count / rowsPerPage) - 1));
  };

  return (
    <Box sx={{ flexShrink: 0, ml: 2.5 }}>
      <IconButton
        onClick={handleFirstPageButtonClick}
        disabled={page === 0}
        aria-label="first page"
      >
        {theme.direction === "rtl" ? <LastPageIcon /> : <FirstPageIcon />}
      </IconButton>
      <IconButton
        onClick={handleBackButtonClick}
        disabled={page === 0}
        aria-label="previous page"
      >
        {theme.direction === "rtl" ? (
          <KeyboardArrowRight />
        ) : (
          <KeyboardArrowLeft />
        )}
      </IconButton>
      <IconButton
        onClick={handleNextButtonClick}
        disabled={page >= Math.ceil(count / rowsPerPage) - 1}
        aria-label="next page"
      >
        {theme.direction === "rtl" ? (
          <KeyboardArrowLeft />
        ) : (
          <KeyboardArrowRight />
        )}
      </IconButton>
      <IconButton
        onClick={handleLastPageButtonClick}
        disabled={page >= Math.ceil(count / rowsPerPage) - 1}
        aria-label="last page"
      >
        {theme.direction === "rtl" ? <FirstPageIcon /> : <LastPageIcon />}
      </IconButton>
    </Box>
  );
}

function createData(id, image, types, height, weight) {
  return { id, image, types, height, weight };
}

export default function PokeTable(type1 = "", type2 = "") {
  const api_url = `https://pokeapi.co/api/v2/pokemon`;
  const [pokeList, setPokeList] = useState([]);
  const [pokeDetails, setPokeDetails] = useState({});
  const [pokeMapDetails, setPokeMapDetails] = useState([]);

  useEffect(() => {
    Fetching(api_url, setPokeList);
  }, []);

  useEffect(() => {
    pokeList.forEach((pokemon, _) => {
      Fetching(
        `https://pokeapi.co/api/v2/pokemon/${GetPokeID(pokemon.url)}/`,
        (data) => {
          setPokeMapDetails((pl) => [...pl, data]);
        }
      );
    });
  }, [pokeList]);

  useEffect(() => {
    console.log(pokeMapDetails);
  }, [pokeMapDetails]);

  const rows = [];

  pokeMapDetails.forEach((pokemon, index) => {
    //const mypoke = Pokemon(pokemon.name);
    rows.push(
      createData(
        pokemon.order,
        <>
          <div>
            <img
              src={`https://raw.githubusercontent.com/PokeAPI/sprites/master/sprites/pokemon/${pokemon.order}.png`}
            />
          </div>
          <div>
            {String(pokemon.name).charAt(0).toUpperCase() +
              String(pokemon.name).slice(1)}
          </div>
        </>,
        <>
          <p>{Capitalize(pokemon.types[0].type.name)}</p>
          <p>{Capitalize(pokemon.types[1]?.type.name)}</p>
        </>,
        pokemon.height * 10 ?? "Loading...",
        pokemon.weight ?? "Loading..."
      )
    );
  });

  rows.sort((a, b) => (a.calories < b.calories ? -1 : 1));

  const [page, setPage] = React.useState(0);
  const [rowsPerPage, setRowsPerPage] = React.useState(5);

  // Avoid a layout jump when reaching the last page with empty rows.
  const emptyRows =
    page > 0 ? Math.max(0, (1 + page) * rowsPerPage - rows.length) : 0;

  const handleChangePage = (
    event: React.MouseEvent<HTMLButtonElement> | null,
    newPage: number
  ) => {
    setPage(newPage);
    //setLimit(limit * newPage)
    //setOffset(limit - 5)
  };

  const handleChangeRowsPerPage = (
    event: React.ChangeEvent<HTMLInputElement | HTMLTextAreaElement>
  ) => {
    setRowsPerPage(parseInt(event.target.value, 10));
    setPage(0);
  };

  return (
    <TableContainer component={Paper}>
      <Table sx={{ minWidth: 0 }} aria-label="custom pagination table">
        <TableHead>
          <TableRow>
            <TableCell>ID</TableCell>
            <TableCell align="left">Pok√©mon</TableCell>
            <TableCell align="left">Tipos</TableCell>
            <TableCell align="left">Altura (cm)</TableCell>
            <TableCell align="left">Peso (Kg)</TableCell>
          </TableRow>
        </TableHead>
        <TableBody>
          {(rowsPerPage > 0
            ? rows.slice(page * rowsPerPage, page * rowsPerPage + rowsPerPage)
            : rows
          ).map((row) => (
            <TableRow key={row.id}>
              <TableCell component="th" scope="row">
                {row.id}
              </TableCell>
              <TableCell style={{ width: 500 }} align="left">
                {row.image}
              </TableCell>
              <TableCell style={{ width: 500 }} align="left">
                {row.types}
              </TableCell>
              <TableCell style={{ width: 500 }} align="left">
                {row.height}
              </TableCell>
              <TableCell style={{ width: 500 }} align="left">
                {row.weight}
              </TableCell>
            </TableRow>
          ))}
          {emptyRows > 0 && (
            <TableRow style={{ height: 53 * emptyRows }}>
              <TableCell colSpan={6} />
            </TableRow>
          )}
        </TableBody>
        <TableFooter>
          <TableRow>
            <TablePagination
              rowsPerPageOptions={5}
              colSpan={3}
              count={rows.length}
              rowsPerPage={rowsPerPage}
              page={page}
              SelectProps={{
                inputProps: {
                  "aria-label": "rows per page",
                },
                native: true,
              }}
              onPageChange={handleChangePage}
              onRowsPerPageChange={handleChangeRowsPerPage}
              ActionsComponent={TablePaginationActions}
            />
          </TableRow>
        </TableFooter>
      </Table>
    </TableContainer>
  );
}
